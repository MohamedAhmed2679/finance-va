export function formatCurrency(amount: number, currency = 'USD', hideAmounts = false): string {
    if (hideAmounts) return '••••';
    const curr = CURRENCIES.find(c => c.code === currency);
    try {
        return new Intl.NumberFormat('en-US', { style: 'currency', currency, minimumFractionDigits: 2 }).format(amount);
    } catch {
        return `${curr?.symbol ?? ''}${amount.toFixed(2)}`;
    }
}

import { useStore } from '../store/useStore';

export function getCategoryInfo(key: string) {
    const cats = useStore.getState().categories;
    return cats.find(c => c.key === key) ?? cats[cats.length - 1];
}

export function getPaymentMethodInfo(key: string) {
    const pms = useStore.getState().paymentMethods;
    return pms.find(p => p.key === key) ?? pms[pms.length - 1];
}

/**
 * Calculates the exact start and end dates (YYYY-MM-DD) for a given active cycle.
 * For example, if cycleStartDay = 25, and targetDate is March 10, the cycle is Feb 25 to Mar 24.
 * If cycleStartDay = 1, it returns the standard calendar month.
 */
export function getActiveCycleDates(targetDate: string | Date, cycleStartDay = 1): { start: string, end: string } {
    const d = new Date(targetDate);
    // If we're on day 15 but cycle starts on 25, we're in the PREVIOUS month's cycle.
    // If we're on day 26 and cycle starts on 25, we're in the CURRENT month's cycle.

    let cycleStartMonth = d.getMonth();
    let cycleStartYear = d.getFullYear();

    if (d.getDate() < cycleStartDay) {
        cycleStartMonth -= 1;
        if (cycleStartMonth < 0) {
            cycleStartMonth = 11;
            cycleStartYear -= 1;
        }
    }

    const start = new Date(cycleStartYear, cycleStartMonth, cycleStartDay);

    // The end date is exactly one month from the start date minus 1 ms (or just day before).
    const end = new Date(cycleStartYear, cycleStartMonth + 1, cycleStartDay - 1);

    // Using simple formatting that avoids timezone shift issues by extracting local dates
    const pad = (n: number) => n.toString().padStart(2, '0');

    return {
        start: `${start.getFullYear()}-${pad(start.getMonth() + 1)}-${pad(start.getDate())}`,
        end: `${end.getFullYear()}-${pad(end.getMonth() + 1)}-${pad(end.getDate())}`
    };
}
